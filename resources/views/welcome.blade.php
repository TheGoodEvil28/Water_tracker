            const revealOnScroll = () => {
                sections.forEach(section => {
                    const sectionTop = section.getBoundingClientRect().top;
                    const triggerBottom = window.innerHeight * 0.85; // Trigger 15% from the bottom of the viewport
                    if (sectionTop < triggerBottom) {
                        section.classList.add('visible'); // Add the 'visible' class when section is in viewport
